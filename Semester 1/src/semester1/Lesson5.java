public class Lesson5 {
	public static void main(String[] args) {
		// for() needs ;;
		// this will run; if this is true;
		// when this runs it will repeat with i++
		// pattern continues until condition is no longer met
		for (int i = 0; i < 5; i++) {
			// printf does not add a next line character like println does
			// %d gets replaced with value of the variable
			// \n means new line
			System.out.printf("The value of i is:  %d\n" + i);
			// %d doesn't work!!
		}
	}

}
